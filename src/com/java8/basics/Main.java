package com.java8.basics;//package name should be in lower case

public class Main {// class name should be in PascalCase

    public static void main(String args[]) {
        // statically type language are compiler friendly
        System.out.print("Hello World\n");
        int numberOne;// variables have camelCaseConvention

        numberOne = 8;
        int numberTwo = 9;
        System.out.println(numberOne + numberTwo);
        System.out.println();
    }

}
